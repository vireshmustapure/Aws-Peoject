const express = require('express');
const app = express();
const cors = require('cors');

app.use(cors());
app.use(express.json());

app.post('/api/greet', (req, res) => {
    const { name } = req.body;
    res.json({ message: `Hello, ${name} from Server!` });
});

app.listen(3000, () => {
    console.log('Server running on port 3000');
});
